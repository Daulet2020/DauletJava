package Arrays;

public class ContinueStatement {
	
	public static void main(String[] args) {
	for (int i = 0; i < 5; i++) {
		if (i>2) {
			continue;
		}
		System.out.print(i+" ");
	} System.out.println();
	
	
	
		
	int [] nums = {1,2,3,4,5};
	for (int j = 0; j < nums.length; j++) {
		if (nums[j]<3) {
			continue;
		}
		System.out.print(nums[j]+" ");
		
	}
	
	
	
	
	
	
	
	
	
	
	
	
	
	
	
		
	}

}
